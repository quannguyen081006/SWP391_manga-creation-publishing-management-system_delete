package manga.enums;

/**
 * Annotation categories for editorial feedback.
 * Used to classify the type of issue being addressed.
 */
public enum AnnotationCategory {
    ART,           // Visual art issues (composition, anatomy, etc.)
    STORY,         // Narrative and plot issues
    PACING,        // Story pacing and flow
    DIALOGUE,      // Dialogue and character voice
    PANELING,      // Panel layout and flow
    TYPOGRAPHY,    // Text and font issues
    OTHER          // Other feedback
}
